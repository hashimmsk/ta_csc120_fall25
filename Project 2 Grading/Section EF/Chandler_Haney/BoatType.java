/**
 * The BoatType enum represents the two possible kinds of boats
 * that the sailing club owns.
 *
 * This keeps boat types safe and consistent in the program:
 * they can only be SAILING or POWER, nothing else.
 */
public enum BoatType {

    /**
     * A sailboat that mainly uses wind and sails to move.
     */
    SAILING,

    /**
     * A power boat that mainly uses an engine or motor to move.
     */
    POWER
}


